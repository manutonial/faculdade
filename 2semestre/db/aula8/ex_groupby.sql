SELECT c.nome, ca.modelo, a.dataAluguel
FROM aluguel AS a
INNER JOIN cliente AS c
ON c.codcliente = a.codcliente
INNER JOIN carro AS ca
ON ca.codcarro = a.codcarro
WHERE dataAluguel BETWEEN'2024-01-12' AND '2025-01-01';

