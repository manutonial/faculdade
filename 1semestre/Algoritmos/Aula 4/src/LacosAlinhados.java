public class LacosAlinhados {
    
    public static void main(String[] args) {
        
        for (int i = 0; i < 5; i++) {
        
            for (int j = 1; j <= 3; j++) {
            
                System.out.println("i: " + i);
                
                System.out.println("     j:" + j);
            }  
        }
    }  
}
